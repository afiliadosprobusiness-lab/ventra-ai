export function getProspectorWorkspaceData(workspaceId: string | null | undefined): ProspectorWorkspaceData {
  const workspace = getWorkspaceById(workspaceId);
  const workspaceProspects = prospects.filter((prospect) => prospect.workspaceId === workspace.id);

  return {
    workspaceName: workspace.name,
    prospects: workspaceProspects,
    audits: workspaceProspects.map((prospect) => prospectAudits.find((audit) => audit.prospectId === prospect.id) ?? buildFallbackAudit(prospect)),
    savedSearches: savedSearches.filter((savedSearch) => savedSearch.workspaceId === workspace.id),
    quickInsights: insightsByWorkspace[workspace.id] ?? [],
    topIndustries: getTopCounts(workspaceProspects, "industry"),
    topLocations: getTopCounts(workspaceProspects, "city"),
  };
}

export function deriveCrmStatus(
  crmStatus: ProspectCrmStatus,
  opportunityStatus: ProspectOpportunityStatus,
  outreachStatus: ProspectOutreachStatus,
) {
  return {
    inCrm: crmStatus !== "Not in CRM",
    hasOpportunity: opportunityStatus === "Opportunity created",
    inOutreach: outreachStatus !== "Not started",
  };
}
