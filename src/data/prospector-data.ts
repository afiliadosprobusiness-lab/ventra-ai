function getTopCounts(items: Prospect[], key: "industry" | "city") {
  return Object.entries(
    items.reduce<Record<string, number>>((accumulator, prospect) => {
      const label = prospect[key];
      accumulator[label] = (accumulator[label] ?? 0) + 1;
      return accumulator;
    }, {}),
  )
    .sort((left, right) => right[1] - left[1])
    .slice(0, 3)
    .map(([label, count]) => ({ label, count }));
}

